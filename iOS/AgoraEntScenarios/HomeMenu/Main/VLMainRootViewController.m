#import "VLMainRootViewController.h"
#import "VLMainTabBarController.h"


@interface VLMainRootViewController ()

@end

@implementation VLMainRootViewController

#pragma mark - Life Cycle Methods
- (void)viewDidLoad {
    [super viewDidLoad];
    self.navigationBarHidden = YES;
    self.view.backgroundColor = [UIColor whiteColor];
    [self createNewTabBar];
}
#pragma mark - Intial Methods
- (void)initSubviews {
    
    [self masLayoutSubViews];
}
- (void)masLayoutSubViews {
    
}
#pragma mark - Private Method
- (CYLTabBarController *)createNewTabBar {
    return [self createNewTabBarWithContext:nil];
}

- (CYLTabBarController *)createNewTabBarWithContext:(NSString *__nullable)context {
    VLMainTabBarController *tabBarController = [[VLMainTabBarController alloc] initWithContext:context];
    self.viewControllers = @[tabBarController];
    return tabBarController;
}

@end
